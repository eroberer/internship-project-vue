<template>
  <router-view></router-view>
</template>

<script>
export default {
  name: "app",

  created ()  {
    if (!sessionStorage.getItem("token")) {
      this.$router.push({ name: "Login" });
    }
  },

  watch: {
    $route: function(value) {
      if (value.name !== "login") {
        if (!sessionStorage.getItem("token")) {
          this.$router.push({ name: "Login" });
        }
      }
    }
  }
};
</script>

